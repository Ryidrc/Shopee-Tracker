import { PricingItem } from './types';

const calculateInitialTotal = (hargaJual: number, biaya1250: number, voucher: number, flashSale: number, promotion: number, affiliate: number, admin: number, ongkir: number) => {
  const percentageFees = (hargaJual * (affiliate + admin + ongkir)) / 100;
  const fixedFees = biaya1250 + voucher + flashSale + promotion;
  return hargaJual - percentageFees - fixedFees;
};

// Helper to create items for all 3 shops based on a single product definition
const createItemsForShops = (
  sku: string, 
  brand: string, 
  name: string, 
  hpp: number, 
  sellPrice: number, 
  displayPrice?: number
): PricingItem[] => {
  const shops = ['shop1', 'shop2', 'shop3'] as const;
  const listPrice = displayPrice || sellPrice; // Default list price to selling price if not provided

  return shops.map(shopId => {
    // Determine default affiliate based on shop if needed, currently uniform
    const affiliate = 5; 
    const admin = 8;
    const ongkir = 4;
    
    // Calculate initial flash sale logic if needed (optional defaults from previous data)
    // For simplicity, we initialize flash sale as 5% of sell price if previously it was around that, 
    // but to keep it clean we'll start at 0 or a simple calculation.
    // Based on previous data, Flash Sale was often populated. Let's set a default 5% flash sale placeholder
    const flashSale = Math.round(sellPrice * 0.05);

    const total = calculateInitialTotal(sellPrice, 1250, 0, flashSale, 0, affiliate, admin, ongkir);

    return {
      id: `p-${sku}-${shopId}`,
      sku: sku,
      shopId: shopId,
      brand: brand,
      productName: name,
      stock: 100,
      price: listPrice,     // Display/Core Price
      priceNet: hpp,        // HPP / Modal
      biaya1250: 1250,
      discount: listPrice - sellPrice, // implied discount
      hargaJual: sellPrice, // Actual Selling Price
      flashSale: flashSale,
      promotion: 0,
      affiliate: affiliate,
      admin: admin,
      ongkir: ongkir,
      total: total,
      voucher: 0,
      voucherExpiry: '', // Initialized as empty
      image: ''
    };
  });
};

